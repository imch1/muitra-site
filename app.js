const SUPPORTED = ["en", "fr", "es"];

/* ---------- Apply a language ---------- */
function applyLang(lang) {
  if (!SUPPORTED.includes(lang)) lang = "en";
  const dict = I18N[lang];
  document.documentElement.lang = lang;

  // text nodes
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    if (dict[key]) el.textContent = dict[key];
  });
  // html nodes (allow <br>, <em>)
  document.querySelectorAll("[data-i18n-html]").forEach((el) => {
    const key = el.getAttribute("data-i18n-html");
    if (dict[key]) el.innerHTML = dict[key];
  });

  // screenshots + alt text
  for (let i = 1; i <= 6; i++) {
    const img = document.getElementById("shot" + i);
    if (img) {
      img.src = `assets/screens/${lang}/${i}.png`;
      img.alt = dict.alt[i - 1] || "";
    }
  }

  // active state on switch
  document.querySelectorAll(".lang-switch button").forEach((b) => {
    b.classList.toggle("active", b.dataset.lang === lang);
  });

  try { localStorage.setItem("muitra_lang", lang); } catch (e) {}
}

/* ---------- Pick initial language ---------- */
function initialLang() {
  let saved = null;
  try { saved = localStorage.getItem("muitra_lang"); } catch (e) {}
  if (saved && SUPPORTED.includes(saved)) return saved;
  const nav = (navigator.language || "en").slice(0, 2).toLowerCase();
  return SUPPORTED.includes(nav) ? nav : "en";
}

/* ---------- Menu (hamburger panel) ---------- */
function initMenu() {
  const toggle = document.getElementById("menuToggle");
  const panel = document.getElementById("menuPanel");
  const overlay = document.getElementById("menuOverlay");
  if (!toggle || !panel) return;
  function setOpen(open) {
    document.body.classList.toggle("menu-open", open);
    toggle.setAttribute("aria-expanded", String(open));
    panel.setAttribute("aria-hidden", String(!open));
    if (overlay) overlay.hidden = !open;
  }
  toggle.addEventListener("click", () =>
    setOpen(!document.body.classList.contains("menu-open"))
  );
  if (overlay) overlay.addEventListener("click", () => setOpen(false));
  // Close after picking a destination, but keep the panel open when only
  // switching language or copying the contact email inside it.
  panel.querySelectorAll(".menu-nav a").forEach((a) =>
    a.addEventListener("click", () => { if (a.id !== "menuContact") setOpen(false); })
  );
  document.addEventListener("keydown", (e) => {
    if (e.key === "Escape") setOpen(false);
  });
}

/* ---------- Contact (copy email + still fire mailto) ---------- */
function initContact() {
  const link = document.getElementById("menuContact");
  if (!link) return;
  const flag = link.querySelector(".menu-copied");
  link.addEventListener("click", () => {
    if (navigator.clipboard && navigator.clipboard.writeText) {
      navigator.clipboard.writeText("muitrapp@gmail.com").catch(() => {});
    }
    if (flag) {
      flag.hidden = false;
      clearTimeout(link._t);
      link._t = setTimeout(() => { flag.hidden = true; }, 2000);
    }
  });
}

/* ---------- Carousel ---------- */
function initCarousel() {
  const track = document.getElementById("carTrack");
  const dotsBox = document.getElementById("carDots");
  const slides = Array.from(track.children);

  // build dots
  slides.forEach((_, i) => {
    const d = document.createElement("button");
    d.type = "button";
    d.addEventListener("click", () => {
      slides[i].scrollIntoView({ behavior: "smooth", inline: "center", block: "nearest" });
    });
    dotsBox.appendChild(d);
  });
  const dots = Array.from(dotsBox.children);

  function currentIndex() {
    const center = track.scrollLeft + track.clientWidth / 2;
    let best = 0, bestDist = Infinity;
    slides.forEach((s, i) => {
      const c = s.offsetLeft + s.offsetWidth / 2;
      const dist = Math.abs(c - center);
      if (dist < bestDist) { bestDist = dist; best = i; }
    });
    return best;
  }
  function syncDots() {
    const idx = currentIndex();
    dots.forEach((d, i) => d.classList.toggle("active", i === idx));
  }
  track.addEventListener("scroll", () => window.requestAnimationFrame(syncDots), { passive: true });

  function step(dir) {
    const idx = currentIndex();
    const next = Math.min(slides.length - 1, Math.max(0, idx + dir));
    slides[next].scrollIntoView({ behavior: "smooth", inline: "center", block: "nearest" });
  }
  document.querySelector(".car-prev").addEventListener("click", () => step(-1));
  document.querySelector(".car-next").addEventListener("click", () => step(1));

  syncDots();
}

/* ---------- Boot ---------- */
document.addEventListener("DOMContentLoaded", () => {
  document.getElementById("year").textContent = new Date().getFullYear();
  document.querySelectorAll(".lang-switch button").forEach((b) => {
    b.addEventListener("click", () => applyLang(b.dataset.lang));
  });
  applyLang(initialLang());
  initMenu();
  initContact();
  initCarousel();

  // Beta CTA: always give feedback by copying the address (mailto still fires
  // for anyone with a mail client configured).
  const EMAIL = "muitrapp@gmail.com";
  const copiedMsg = document.getElementById("copiedMsg");
  function flashCopied() {
    if (!copiedMsg) return;
    copiedMsg.hidden = false;
    clearTimeout(flashCopied._t);
    flashCopied._t = setTimeout(() => { copiedMsg.hidden = true; }, 2200);
  }
  function copyEmail() {
    if (navigator.clipboard && navigator.clipboard.writeText) {
      navigator.clipboard.writeText(EMAIL).then(flashCopied, () => {});
    } else {
      const t = document.createElement("textarea");
      t.value = EMAIL; document.body.appendChild(t); t.select();
      try { document.execCommand("copy"); flashCopied(); } catch (e) {}
      document.body.removeChild(t);
    }
  }
  const betaEmail = document.getElementById("betaEmail");
  if (betaEmail) betaEmail.addEventListener("click", copyEmail);
});
